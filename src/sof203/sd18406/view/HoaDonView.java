    private void loadData(ArrayList<HoaDon> list) {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tblHoaDon.getModel();
        defaultTableModel.setRowCount(0);
        for (HoaDon hd : list) {
            defaultTableModel.addRow(new Object[]{
                hd.getId(),
                hd.getTen(),
                hd.getSoLuong(),
                hd.getLoaiVe(),
                hd.thanhTien()
            });
        }
    }
